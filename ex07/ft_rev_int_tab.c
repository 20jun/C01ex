void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int temp[size];

	i = 1;
	while (i <= size)
	{
		temp[i - 1] = tab[size - i];
		i++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = temp[i];
		i++;
	}
}
